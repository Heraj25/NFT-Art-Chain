const hre = require("hardhat");

async function main() {
  const [deployer] = await hre.ethers.getSigners();

  const NFTContract = await hre.ethers.getContractFactory("NFT");
  const nftContract = await NFTContract.attach("0x5FbDB2315678afecb367f032d93F642f64180aa3");

  const tokenId = 1; // Replace with the actual tokenId you want to query

  // Call the contract's getTokenId function using the callStatic method
  const tokenToId = await nftContract.callStatic.getTokenId(tokenId);

  console.log("Token ID:", tokenToId);
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
});



